/**
 * The Shape class is an abstract base class that represents a geometric shape.
 * It provides two abstract methods, draw() and erase(), which must be
 * implemented
 * by any concrete subclass. These methods are intended to define the behavior
 * for drawing and erasing the shape, respectively.
 */
public abstract class Shape {
    // Abstract method without implementation
    public abstract void draw();

    // Abstract method without implementation
    public abstract void erase();
}
